#!/usr/bin/env node
const fs = require('fs');
const path = require('path');
const root = path.resolve(__dirname, '..');
const read = (rel) => fs.readFileSync(path.join(root, rel), 'utf8');
const failures = [];
const req = (s, n, m) => { if (!s.includes(n)) failures.push(m); };
const backendTypes = read('backend/src/services/designCore.types.ts');
const backendTruth = read('backend/src/services/designCore/designCore.reportDiagramTruth.ts');
const backendService = read('backend/src/services/designCore.service.ts');
const exportService = read('backend/src/services/exportDesignCoreReport.service.ts');
const frontendTypes = read('frontend/src/lib/designCoreSnapshot.ts');
const frontendTruth = read('frontend/src/lib/reportDiagramTruth.ts');
req(backendTypes, 'BackendReportTruthModel', 'Backend types must define BackendReportTruthModel.');
req(backendTypes, 'BackendDiagramTruthModel', 'Backend types must define BackendDiagramTruthModel.');
req(backendTypes, 'reportTruth: BackendReportTruthModel', 'DesignCoreSnapshot must expose backend reportTruth.');
req(backendTypes, 'diagramTruth: BackendDiagramTruthModel', 'DesignCoreSnapshot must expose backend diagramTruth.');
req(backendTruth, 'buildBackendReportTruthModel', 'Backend report truth builder missing.');
req(backendTruth, 'buildBackendDiagramTruthModel', 'Backend diagram truth builder missing.');
req(backendTruth, 'implementationReviewQueue', 'Backend report truth must include implementation review queue.');
req(backendTruth, 'verificationCoverage', 'Backend report truth must include verification coverage.');
req(backendService, 'buildBackendReportTruthModel', 'Design core must build backend report truth.');
req(backendService, 'reportTruth,', 'Snapshot return must include reportTruth.');
req(backendService, 'diagramTruth,', 'Snapshot return must include diagramTruth.');
req(exportService, 'Phase 38 Backend Report Truth Summary', 'Export must include backend report truth.');
req(exportService, 'Phase 39 Backend Diagram Render Truth Summary', 'Export must include backend diagram truth.');
req(frontendTypes, 'reportTruth?: BackendReportTruthModel', 'Frontend snapshot must type backend reportTruth.');
req(frontendTypes, 'diagramTruth?: BackendDiagramTruthModel', 'Frontend snapshot must type backend diagramTruth.');
req(frontendTruth, 'if (designCore?.reportTruth)', 'Frontend must prefer backend reportTruth.');
req(frontendTruth, 'if (designCore?.diagramTruth)', 'Frontend must prefer backend diagramTruth.');
if (failures.length) { console.error('Backend report/diagram truth check failed:'); failures.forEach((f) => console.error(' - '+f)); process.exit(1); }
console.log('Backend report/diagram truth check passed.');
